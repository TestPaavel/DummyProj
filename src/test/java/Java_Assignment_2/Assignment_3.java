public class Assignment_3 {  //Create a calculator app  to add,subtract, Multiply and Divide
	
	public int Add(int a,int b)
	
	{
		int c=a+b;
		return c;
	}
	
	public int Subtract(int a, int b)
	{
		int c=a-b;
		return c;
	}
	
	public int Multiply(int a, int b)
	{
		int c=a*b;
		return c;
	}
	
	public int Divide(int a, int b)
	{
		int c=a/b;
		return c;
	}

}
